                <section id="esg">
                    <h2 class="greentitle"><img src="/common/img/g_icon01.png" class="gicon01"><span>E（environment）、S（social）、G（governance）</span></h2>
                    <div class="greeninfo">
                        <div class="whiteinfoDetail">
                            <p class="title">(1)About ESG</p>
                            Recently, investors are becoming to consider ESG Factors, <span>which consists of Environment, Social and Governance,</span> when selecting to invest in equities and debts.
                            <br>
                            <br> Considering that these non-financial information would affect companies’ long term performance, ESG investment, which considers ESG factors into its investment, are increasingly spreading out globally. It is said that the global ESG related investment volume is more than 20 trillon dollars now.
                            <br>
                            <br> In Japan, ESG investments are taking growing interest from investors, e.g. Government Pension Investment Fund (GPIF) started passive management based on ESG index.
                            <p class="pt20"><img src="/en/common/img/g_esg.png" alt="ESG"></p>
                        </div>
                    </div>
                </section>
                <section>
                    <h2 class="greentitle"><img src="/common/img/g_icon01.png" class="gicon01"><span>Credit rating and ESG</span></h2>
                    <div class="greeninfo">
                        <div class="whiteinfoDetail">
                            There are opinions that credit rating agencies should increase its transparency in how to incorporate ESG factors into its credit rating analysis, amid ESG investments increase. Reflecting such needs, United Nations Principles for Responsible Investment (UNPRI) shall established an initiative names ESG in Credit Ratings in 2016. This initiative aims to improve the transparency in how credit rating agencies systematically incorporate ESG factors into its credit analysis to share the opinions between investors and credit rating agencies.
                            <br>
                            <br> JCR signed this initiative for the first time as a Japanese credit rating agency and is participating in global discussions regarding this issue.
                            <br>
                            <p class="greenarea">For more details about this initiative, please <a href="/en/pdf/greenfinance/Statement-on-ESG-in-Credit-Ratings_2017(EN)_r.pdf" target="_blank" class="greenlink">click here.<img src="/common/img/pdf01.gif" alt="PDF"></a></p>
                            <p class="title">JCR's Credit Ratings and ESG Factors</p>
                            JCR believes that it is necessary to consider ESG factors when it assesses each items in its credit rating methodlogies. JCR includes the following impacts in its analysis concerning the possibility of ESG factor’s affecting any impacts on credit ratings.
                            <br>
                            <br> - Impact by ESG factors on issuers’ sustainability
                            <br> - Impact by ESG factors on cash flow generation capacity required for debt redemption
                            <br> - Impact by initiatives for ESG on fundraising ability
                            <br>
                            <p class="greenarea">For more details, please <a href="/en/pdf/greenfinance/Necessity-to-consider-ESG-factors-into-credit-ratings(EN).pdf" target="_blank" class="greenlink">click here.<img src="/common/img/pdf01.gif" alt="PDF"></a></p>
