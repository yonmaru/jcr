                <section id="greenfinance" class="mt30">
                    <h2 class="greentitle"><img src="/common/img/g_icon01.png" class="gicon01"><span>JCR’s Efforts to contribute to Green/Social Finance</span></h2>
                    <div class="greeninfo">
                        <p class="greeninfoDetail">
                            Amid global environmental issues are getting severer, such as climate changes, green bond issuances are sharply increasing mainly in Europe and the U.S. Reflecting such global movements, stakeholders in Japanese markets are increasingly interested in green bond issuances in Japan.
                            <br>In addition, issuing social bonds are also increasing globally, reflecting the demand of certain amount of investors who want to contribute specifically to social issues among ESG investments.
                            <br>
                            <br>Under such situations, JCR have been thinking what we could do for realizing a sustainable society for next generations as a responsibility of a company, at the same time, as a credit rating agency as one of the participants in the financial market.
                            <br>
                            <br>From such considerations, <span>JCR has launched the provision of green bond/social bond evaluation services to whom it may be considering the issuance of green bonds/social bonds.</span>
                            <br>
                            <br>We believe the third party evaluation would help the issuers to reduce costs and times to take administrative procedures for preparing the green bond/social bond issuance.
                            <br>JCR will provide well detailed evaluation services in accordance with ICMA’s Green Bond/Social Bond Principles and Ministry of Environment’s Green Bond Guidelines.
                            <br>
